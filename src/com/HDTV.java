package com;

//	POJO Class HDTV which contains properties defining the same.
public class HDTV {
	//	Properties of HDTV
	private String brandName;
	private long size;

	//	Getters and Setters of Properties for HDTV where it is used access the private properties of HDTV
	public String getBrandName() {
		return brandName;
	}
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	public long getSize() {
		return size;
	}
	public void setSize(long size) {
		this.size = size;
	}
}
